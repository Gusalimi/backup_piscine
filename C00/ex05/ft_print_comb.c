#include <unistd.h>

void	ft_print_comb(void)
{
	char	ltrs[3];

	ltrs[0] = '0';
	while (ltrs[0] <= '9')
	{
		ltrs[1] = ltrs[0] + 1;
		while (ltrs[1] <= '9')
		{
			ltrs[2] = ltrs[1] + 1;
			while (ltrs[2] <= '9')
			{
				write(1, &ltrs[0], 1);
				write(1, &ltrs[1], 1);
				write(1, &ltrs[2], 1);
				if (ltrs[0] != '7' || ltrs[1] != '8' || ltrs[2] != '9')
				{
					write(1, ", ", 2);
				}
				ltrs[2]++;
			}
			ltrs[1]++;
		}
		ltrs[0]++;
	}
}
