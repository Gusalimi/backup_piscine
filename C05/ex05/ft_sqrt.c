int	ft_rec(int nb, int i)
{
	if (nb > 2147395600)
		return (0);
	if (i * i == nb)
		return (i);
	if (i * i > nb)
		return (0);
	return (ft_rec(nb, i + 1));
}

int	ft_sqrt(int nb)
{
	return (ft_rec(nb, 0));
}
