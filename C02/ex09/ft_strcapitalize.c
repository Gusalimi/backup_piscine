#include <stdio.h>

char	*ft_strcapitalize(char *str)
{
	char	p;
	int		i;

	p = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (p < 48 || (p > 57 && p < 65) || (p > 90 && p < 97) || p > 122)
		{
			if (str[i] >= 97 && str[i] <= 122)
				str[i] -= 32;
		}
		else
		{
			if (str[i] >= 65 && str[i] <= 90)
				str[i] += 32;
		}
		p = str[i];
		i++;
	}
	return (str);
}
